package JAVA基础深化和提高.常用类;

public class TestMath {
    public static void main(String[] args) {
        //取整相关操作
        System.out.println(Math.ceil(3.9));    //大于它的最小整数
        System.out.println(Math.floor(3.9));   //小于它的最大整数
        System.out.println(Math.round(3.2));  //四舍五入
        System.out.println(Math.abs(-45));    //绝对值
        System.out.println(Math.sqrt(64));    //开方
        System.out.println(Math.pow(5,2));    //a的b次幂
        System.out.println(Math.PI);          //常量
        System.out.println(Math.E);
        System.out.println(Math.random());    //0-2之间的随机数
    }
}
